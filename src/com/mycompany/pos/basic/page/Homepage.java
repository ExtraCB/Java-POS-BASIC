    public void type_Setup(){
        ConnectDb con = new ConnectDb();
        DefaultTableModel model = con.getAllType();
        
        type_table.setModel(model);
    }
    
    public TypeOb[] product_Setup(){
        ConnectDb con = new ConnectDb();
        TypeOb typ[] = con.getType();
        
        for(TypeOb ty : typ){
            type_pro_input.addItem(ty.getName());
        }
        return typ;
    }
    
