    private void create_productActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_productActionPerformed
       TypeOb typ[] = product_Setup();
       TypeOb typs = null;
       
       String name = name_pro_input.getText();
       double price = Double.parseDouble(price_pro_input.getText());
       String typ_name = type_pro_input.getSelectedItem().toString();
       
       for(TypeOb ts : typ){
           if(ts.getName().equals(typ_name)){
               typs = new TypeOb(ts.getId(),ts.getName());
           }
       }
       
       ProductOb pro = new ProductOb(name,price,typs);
       
       ConnectDb con = new ConnectDb();
       boolean status = con.create_Product(pro);
       
       if(status){
            JOptionPane.showMessageDialog(this, "Create Success");
        }else{
            JOptionPane.showMessageDialog(this, "Name is already to use !");
        }
       
       product_show();
    }//GEN-LAST:event_create_productActionPerformed

