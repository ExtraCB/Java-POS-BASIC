    public void add_to_Table(String name,double price){
        String quantity = quantity_input.getText();
        if(quantity.equals("")){
            quantity = JOptionPane.showInputDialog(null,"Please Enter Quantity","1");
        }
        
        Double tqty = Double.valueOf(quantity);
        Double To_price = price * tqty;
        System.out.println("Total : " + To_price);
        
        DefaultTableModel dt = (DefaultTableModel) cart_table.getModel();
            Vector v = new Vector();
            v.add(name);
            v.add(tqty);
            v.add(To_price);
            dt.addRow(v);
        
    }
    
    public ProductOb selected_table(){
        
        
        ProductOb pros = null;
        int row = pro_table1.getSelectedRow();
        System.out.println(row);
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Please Selected Item");
        }else{
            String Name = pro_table1.getValueAt(row, 1).toString();
            Double Price = Double.valueOf(pro_table1.getValueAt(row,2).toString());
            pros = new ProductOb(Name,Price);
            return pros;
        }
        return pros;
    }

